    private void vDetailsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_vDetailsMouseClicked
        javax.swing.table.TableModel om = CustomerOrder.getModel();
        Double total = 0.0;
        int[] indexs = CustomerOrder.getSelectedRows();
        Object[] row = new Object[1];
        for (int i = 0; i < indexs.length; i++) {
            vieworder = (String) om.getValueAt(indexs[i], 0);
        }
        javax.swing.table.DefaultTableModel dt = new javax.swing.table.DefaultTableModel();
        ViewTable.setModel(dt);

        dt.addColumn("Food/Drinks Code");
        dt.addColumn("Food/Drinks Name");
        dt.addColumn("Food/Drinks Price");
        try {

            File fpath = new File("DetailsFile\\CustomerOrder");
            File f = new File(fpath, vieworder + ".txt");
            BufferedReader br;
            br = new BufferedReader(new FileReader(f));

            Object[] tableLines = br.lines().toArray();

            String line1 = tableLines[0].toString().trim();
            String[] dataRow1 = line1.split("/");
            nama.setText(" " + dataRow1[0]);
            nombor.setText(dataRow1[1]);
            date.setText(dataRow1[2]);
            time.setText(dataRow1[3]);

            for (int i = 1; i < tableLines.length; i++) {
                String line = tableLines[i].toString().trim();
                String[] dataRow = line.split("/");
                dt.addRow(dataRow);
            }
            for (int i = 1; i < tableLines.length; i++) {
                String line = tableLines[i].toString();
                String[] dataRow = line.split("/");
                total = total + Double.parseDouble(dataRow[2]);
            }
            totalprice = String.valueOf(total);
            ttprice.setText("RM " + totalprice);

            br.close();

        } catch (FileNotFoundException ex) {
            Logger.getLogger(User.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Edit.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_vDetailsMouseClicked

    private void completeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_completeMouseClicked

        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) CustomerOrder.getModel();
        int[] rows = CustomerOrder.getSelectedRows();
        for (int i = 0; i < rows.length; i++) {
            
            String u = (String) model.getValueAt(rows[i], 0);
            File f = new File("DetailsFile/CustomerOrder/" + u + ".txt");
            f.renameTo(new File("DetailsFile/CompletedOrder/" + u + ".txt"));
            
            model.removeRow(rows[i] - i);
        }
        
            javax.swing.table.DefaultTableModel dt = new javax.swing.table.DefaultTableModel();
            ViewTable.setModel(dt);

            dt.addColumn("Food/Drinks Code");
            dt.addColumn("Food/Drinks Name");
            dt.addColumn("Food/Drinks Price");
            dt.setRowCount(0);
            nama.setText("");
            nombor.setText("");
            ttprice.setText("");
       

        
    }//GEN-LAST:event_completeMouseClicked

