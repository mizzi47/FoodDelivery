    public void getFilesName() {
        File file = new File("DetailsFile/CustomerOrder");
        File[] files = file.listFiles();
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) CustomerOrder.getModel();
        model.setColumnIdentifiers(new String[]{"Customer Orders"});

        Arrays.sort(files, new Comparator<File>() {
            public int compare(File f1, File f2) {
                return Long.valueOf(f1.lastModified()).compareTo(f2.lastModified());
            }
        });
        Object[] row = new Object[1];
        model.setRowCount(0);

        for (int i = 0; i < files.length; i++) {
            row[0] = files[i].getName().replaceFirst("[.][^.]+$", "");
            model.addRow(row);
        }
    }

